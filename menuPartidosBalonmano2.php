	function datosIniciales() {
		// Establecer Competición
		var competicion = leerCookie("competicion");
		var texto = "<label>"+competicion+"</label>";
		$("#labelCompeticion").html(texto);

		// Establecer Sección
		var seccion = leerCookie("seccionCompeticion");
		if (seccion == "M") {
			var nombreSeccion = "Secc. Masculina";
		}else if (seccion == "F") {
			var nombreSeccion = "Secc. Femenina";
		}
		var texto = "<label>"+nombreSeccion+"</label>";
		$("#labelSeccion").html(texto);

		if (competicion == "Copa del Rey") {
			$("#liga").hide();
			$("#superCopa").hide();
			$("#copa").show();
		}else if (competicion == "Supercopa España") {
			$("#liga").hide();
			$("#copa").hide();
			$("#superCopa").show();
		}else {
			$("#copa").hide();
			$("#superCopa").hide();
			$("#liga").show();
		}
	}

	/* Funciones con cookies */
	function crearCookie(nombreCookie, valorCookie) {
	  var expires = "max-age=3000"; //Segundos para que se destruya la cookie
	  var path = "path=/;domain=mijonsu.com;secure"; //Ruta en la que se puede utilizar la cookie de modo seguro
	  //document.cookie = nombreCookie + "=" + valorCookie + "; " + expires + "; " + path;
	  document.cookie = nombreCookie + "=" + valorCookie + "; " + expires;
	};

	function leerCookie(nombreCookie) {
		var misCookies = document.cookie.split(';');

		if (misCookies.length > 1) {
			for (i in misCookies) {
				var buscar = misCookies[i].search(nombreCookie);
				if (buscar > -1) {
					var cookieRequerida = misCookies[i];
				}
			}

			if (cookieRequerida) {
				var igual = cookieRequerida.indexOf("=");
				var valor = cookieRequerida.substring(igual+1);

				return valor;	
			}
		}
	};

	function eliminarCookie(nombreCookie) {
	  return document.cookie = nombreCookie + '=;expires=Thu, 01 Jan 1970 00:00:01 GMT;';
	};
